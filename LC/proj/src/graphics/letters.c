#include <auxiliary/debug.h>

/**
 * @brief Turns a character into a xpm, if available
 * @param int c : character (as an integer)
 * @return const char* const* : XPM
 */
const char*const* get_char_xpm(int c) {
    const char *const* result = char_xpm[c];
    DEBUG_PRINTF("%d (\"%c\")\n", c, c);
    if (result[0] == '\0') {// no XPM was created for it
        DEBUG_PRINTF("Character %d (\"%c\") has no XPM\n", c, c);
        return char_inval;
    }
    return result;
}

#include <graphics/sprite.h>

/**
 * @brief Load all character sprites
 * @param sprite_t result[256] : where to place the sprites
 * @return int : 0 on success
 */
int load_char_sprites(sprite_t result[256]) {
    for (int c = 0; c < 256; ++c) {
        if (load_sprite_from_xpm(get_char_xpm(c), result + c)) {
            for (int i = 0; i < c; ++i)
                free(result[i].pixels);
            return -1;
        }
    }
    return 0;
}
